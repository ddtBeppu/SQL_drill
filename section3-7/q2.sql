/*
アクセス記録テーブル（access_log）からアクセス日時が2012年06月01日より前か、リンク元のURLが空である情報を削除しましょう。
*/

/** 解答 **/
DELETE FROM -- テーブルから値を削除
    access_log -- アクセス記録テーブル
WHERE -- 条件で絞りこみ
    (
        access_date < '2012-06-01' -- アクセス日時が2012年06月01日より前
    OR -- または
        referer IS NULL -- リンク元のURLが空
    )
;

