/*
貸し出し記録テーブル（rental）から貸出日が2011年12月31日以前で、かつ、返却済みである情報について削除してみましょう。
*/

/** 解答 **/
DELETE FROM -- テーブルからデータを削除
    rental -- 貸し出し記録テーブル
WHERE -- 条件で絞り込み
    (
        rental_date <= '2011-12-31' -- 貸出日が2011年12月31日以前
    AND -- かつ
        returned = 1 -- 返却済み
    )
;

